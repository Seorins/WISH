export function getVillagerLines({
  npcId,
  key,
  fallback,
}: {
  npcId: VillagerNpcId
  key?: VillagerLineKey
  fallback: string[]
}): string[] {
  if (!key) return fallback

  const variants = VILLAGER_LINE_PACK[npcId]?.[key]
  if (!variants || variants.length === 0) return fallback

  const index = Math.floor(Math.random() * variants.length)
  return variants[index]
}
